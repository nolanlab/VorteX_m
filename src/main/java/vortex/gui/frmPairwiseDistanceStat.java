    private class ValidationComputer extends SwingWorker<DefaultTableModel, Entry<String, NormalStatistics>> {

        private Datapoint[] dp;
        private DefaultTableModel dtm;
        private DistanceMatrix mtx;
        private String title;
        private boolean print = false;

        @Override
        protected void done() {
            mtx = null;
        }

        public ValidationComputer(String title, Datapoint[] dp, DefaultTableModel dtm, DistanceMatrix mtx, boolean print) {
            this.title = title;
            this.dp = dp;
            this.dtm = dtm;
            this.mtx = mtx;
            this.print = print;
        }

        @Override
        protected DefaultTableModel doInBackground() throws Exception {

            ArrayList<Double> al = new ArrayList<Double>();
            for (int i = 0; i < dp.length; i++) {
                for (int j = i + 1; j < dp.length; j++) {

                    if (print & (Math.random() < 0.005)) {
                        logger.print(mtx.getValue(dp[i], dp[j]));
                    }

                    al.add(mtx.getValue(dp[i], dp[j]));
                }
            }
            NormalStatistics ns = new NormalStatistics(al);
            publish(new DefaultEntry[]{new DefaultEntry(title, ns)});
            cmdComputeRatios.setText("Compute");
            cmdComputeRatios.setEnabled(true);
            return dtm;
        }

        @Override
        protected void process(List<Entry<String, NormalStatistics>> chunks) {
            for (Entry<String, NormalStatistics> m : chunks) {
                dtm.addRow(new Object[]{m.getKey(), m.getValue().getMean(), m.getValue().getSD()});
            }
        }
    }
