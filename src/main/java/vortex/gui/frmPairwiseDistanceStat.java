    private void cmdComputeRatiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdComputeRatiosActionPerformed
        try {
            Class<? extends DistanceMeasure> c = ((vortex.util.ClassWrapper) cmbDistanceMeasure.getSelectedItem()).item;

            DistanceMeasure dm = c.getConstructor().newInstance();

            if (computer != null) {
                computer.cancel(true);
            }

            cmdComputeRatios.setText("Computing...");
            cmdComputeRatios.setEnabled(false);

            dm.init(css.getDataset());

            DefaultTableModel dtm = ((DefaultTableModel) jTable1.getModel());

            int rowCnt = dtm.getRowCount();
            for (int i = 0; i < rowCnt; i++) {
                dtm.removeRow(i);
            }

            DistanceMatrix dmtx = new DistanceMatrix(css.getDataset(), dm);

            for (Cluster clus : css.getClusters()) {
                Datapoint[] dp = new Datapoint[clus.size()];
                for (int i = 0; i < clus.getClusterMembers().length; i++) {
                    dp[i] = clus.getClusterMembers()[i].getDatapoint();
                }
                computer = new ValidationComputer(clus.toString(), dp, dtm, dmtx, false);
                computer.execute();
            }
            //logger.print("Total dataset:");
            computer = new ValidationComputer("Total dataset", css.getDataset().getDatapoints(), dtm, dmtx, false);
            computer.execute();


        } catch (Exception e) {
            logger.showException(e);
        }
    }//GEN-LAST:event_cmdComputeRatiosActionPerformed

